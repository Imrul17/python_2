mark=int(input("please enter your mark : "))
if mark>=90:
    print("A+")

elif mark>=80:
    print("A-")

elif mark>=70:
    print("A")

elif mark>=60:
    print("B")

elif mark>=50:
    print("B")

elif mark>=40:
    print("B-")

elif mark>=30:
    print("C")

else:
    print("Fail")